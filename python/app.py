
# print 출력 명령어 
# 문자는 따옴표로 감쌈
print('hello world')

# 숫자는 그냥써도 됨
print(1123)

# 계산식도 가능
print(1+1)

# 사칙연산 기호쓰면 이런것도 됨
# print('점심 나가서 먹을것 같아 ' * 100)

# 자료가 길 경우, 변수를 만들어 사용할 수 있다.
# 변수명 = 변수에 들어갈 자료
name = '국힙원탑 아이유'

# 자료에서 일부만 출력하고 싶을때, 순번을 입력하면 된다

# name의 0번째 문자 출력함
print(name[0])

# name의 2번부터 4번까지 출력함
print(name[2:4])

